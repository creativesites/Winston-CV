/**
 * WP Guard - app.js
 * Handles the logic for generating, displaying, and exporting security snippets.
 * Scoped for the WordPress Admin context.
 */

const SNIPPETS = {
    clean_header: {
        title: 'Clean WP Header',
        code: `// Remove WordPress version number
remove_action('wp_head', 'wp_generator');

// Remove WLW Manifest link
remove_action('wp_head', 'wlwmanifest_link');

// Remove RSD link (EditURI)
remove_action('wp_head', 'rsd_link');

// Remove Shortlink
remove_action('wp_head', 'wp_shortlink_wp_head');

// Remove REST API link from head
remove_action('wp_head', 'rest_output_link_wp_head');`
    },
    disable_editor: {
        title: 'Disable File Editor',
        code: `// Disable the theme and plugin file editor in the admin panel
if (!defined('DISALLOW_FILE_EDIT')) {
    define('DISALLOW_FILE_EDIT', true);
}`
    },
    disable_xmlrpc: {
        title: 'Disable XML-RPC',
        code: `// Completely disable XML-RPC
add_filter('xmlrpc_enabled', '__return_false');

// Disable X-Pingback in headers
add_filter('wp_headers', function($headers) {
    unset($headers['X-Pingback']);
    return $headers;
});`
    },
    restrict_rest: {
        title: 'Restrict REST API',
        code: `// Restrict REST API access to authenticated users only
add_filter('rest_authentication_errors', function($result) {
    if (true === $result || is_wp_error($result)) {
        return $result;
    }
    if (!is_user_logged_in()) {
        return new WP_Error('rest_not_logged_in', 'REST API restricted to authenticated users.', array('status' => 401));
    }
    return $result;
});`
    },
    generic_errors: {
        title: 'Generic Login Errors',
        code: `// Replace detailed login errors with a generic message to prevent username enumeration
add_filter('login_errors', function() {
    return 'Invalid login credentials. Please try again.';
});`
    },
    disable_hints: {
        title: 'Disable Login Hints',
        code: `// Remove the login shake effect
add_action('login_head', function() {
    remove_action('login_head', 'wp_shake_js', 12);
});`
    },
    disable_emojis: {
        title: 'Disable Emojis',
        code: `// Disable WordPress emojis
add_action('init', function() {
    remove_action('admin_print_styles', 'print_emoji_styles');
    remove_action('wp_head', 'print_emoji_detection_script', 7);
    remove_action('admin_print_scripts', 'print_emoji_detection_script');
    remove_action('wp_print_styles', 'print_emoji_styles');
    remove_filter('wp_mail', 'wp_staticize_emoji_for_email');
    remove_filter('the_content_feed', 'wp_staticize_emoji');
    remove_filter('comment_text_rss', 'wp_staticize_emoji');
    add_filter('tiny_mce_plugins', function($plugins) {
        return is_array($plugins) ? array_diff($plugins, array('wpemoji')) : array();
    });
});`
    },
    limit_heartbeat: {
        title: 'Limit Heartbeat',
        code: `// Reduce Heartbeat API frequency to 60 seconds (improves server performance)
add_filter('heartbeat_settings', function($settings) {
    $settings['interval'] = 60;
    return $settings;
});`
    },
    disable_author_archive: {
        title: 'Disable Author Archive',
        code: `// Disable author archives to prevent username discovery via ?author=1
add_action('template_redirect', function() {
    if (is_author()) {
        global $wp_query;
        $wp_query->set_404();
        status_header(404);
    }
});`
    }
};

document.addEventListener('DOMContentLoaded', () => {
    const container = document.getElementById('wp-guard-app');
    if (!container) return;

    const codeOutput = container.querySelector('#code_output code');
    const toggles = container.querySelectorAll('.toggle-item input');
    const copyBtn = container.querySelector('#copy_code');
    const downloadBtn = container.querySelector('#download_plugin');

    // Initialize display
    generateCode();

    // Event Listeners
    toggles.forEach(toggle => {
        toggle.addEventListener('change', generateCode);
    });

    copyBtn.addEventListener('click', copyToClipboard);
    downloadBtn.addEventListener('click', downloadAsPlugin);

    function generateCode() {
        let code = `<?php\n/**\n * WordPress Security Hardening Snippets\n * Generated by WP Guard Custom Tool\n */\n\n`;
        
        let checkedAny = false;
        toggles.forEach(toggle => {
            if (toggle.checked) {
                const snippet = SNIPPETS[toggle.id];
                if (snippet) {
                    code += `// --- ${snippet.title} ---\n${snippet.code}\n\n`;
                    checkedAny = true;
                }
            }
        });

        if (!checkedAny) {
            code += `// Select options on the left to generate security snippets.`;
        }

        // Apply custom syntax highlighting
        codeOutput.innerHTML = highlightCode(code);
    }

    function highlightCode(code) {
        // Simple regex-based highlighter for PHP
        return code
            .replace(/&/g, "&amp;")
            .replace(/</g, "&lt;")
            .replace(/>/g, "&gt;")
            .replace(/(\/\/.+)/g, '<span class="code-comment">$1</span>')
            .replace(/(\/\*.+?\*\/)/gs, '<span class="code-comment">$1</span>')
            .replace(/\b(function|return|if|global|add_action|add_filter|remove_action|remove_filter|define|defined|status_header|new|array|unset)\b/g, '<span class="code-keyword">$1</span>')
            .replace(/('.+?')/g, '<span class="code-string">$1</span>')
            .replace(/(\$[a-zA-Z_]\w*)/g, '<span class="code-variable">$1</span>')
            .replace(/\b([a-zA-Z_]\w*)(?=\()/g, '<span class="code-function">$1</span>');
    }

    async function copyToClipboard() {
        const text = codeOutput.innerText;
        try {
            await navigator.clipboard.writeText(text);
            const originalText = copyBtn.innerText;
            copyBtn.innerText = 'Copied!';
            copyBtn.classList.remove('btn-primary');
            copyBtn.style.backgroundColor = '#28a745';
            
            setTimeout(() => {
                copyBtn.innerText = originalText;
                copyBtn.classList.add('btn-primary');
                copyBtn.style.backgroundColor = '';
            }, 2000);
        } catch (err) {
            console.error('Failed to copy: ', err);
        }
    }

    function downloadAsPlugin() {
        const text = codeOutput.innerText;
        const pluginHeader = `<?php\n/**\n * Plugin Name: WP Guard Hardening\n * Description: Custom security hardening snippets for WordPress.\n * Version: 1.0\n * Author: Winston Zulu\n */\n\n`;
        const fullCode = text.startsWith('<?php') ? text.replace('<?php', pluginHeader) : pluginHeader + text;

        const blob = new Blob([fullCode], { type: 'application/x-httpd-php' });
        const url = URL.createObjectURL(blob);
        const a = document.createElement('a');
        a.href = url;
        a.download = 'wp-guard-hardening.php';
        document.body.appendChild(a);
        a.click();
        document.body.removeChild(a);
        URL.revokeObjectURL(url);
    }
});
